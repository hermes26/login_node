const mongoose = require('mongoose');

const UserSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        default: Date.now
    },
});

//create a model from our schema
const User = mongoose.model('User', UserSchema); //model name: User; model schema: UserSchema

module.exports = User;